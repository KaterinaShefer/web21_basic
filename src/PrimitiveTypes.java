public class PrimitiveTypes {
    public static void main(String[] args) {
        int i = 1;
        byte b = 2;
        short s = 3;
        long l = 4;
        boolean b1 = false;
        boolean b2 = true;
        String s1 = "Это прямая речь!";
        String s2 = "!";
        String s3 = "&";
        char c1 = '!';
        char c2 = '?';
        double d1 = 3.14;
        float f1 = (float) 3.14;
    }
}

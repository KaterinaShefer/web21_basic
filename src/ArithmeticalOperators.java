public class ArithmeticalOperators {
    public static void main(String[] args) {
        exampleOne();
    }

    public static void exampleOne() {
        // "+"
        // "-"
        // "/"
        // "*"
        // "%"
        int a = 10;
        a = a + 5;
        System.out.println(a);
        // ++
        // увеличение на единицу
        a = a + 1;
        // увеличение на единицу
        a++;
        System.out.println(a);
        //  увеличение на единицу
        a += 1;
        System.out.println(a);
    }
}
